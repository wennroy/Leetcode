# 简单做法：深度搜索+暴力枚举匹配

# Definition for a binary tree node.
# class TreeNode:
#     def __init__(self, val=0, left=None, right=None):
#         self.val = val
#         self.left = left
#         self.right = right
class Solution:
    def isSubtree(self, root: TreeNode, subRoot: TreeNode) -> bool:
        def isequal(root1: TreeNode, root2: TreeNode) -> bool:
            if not root1 and not root2:
                return True
            elif not root1 or not root2:
                return False

            if root1.val != root2.val:
                return False
            ans1, ans2 = isequal(root1.left, root2.left), isequal(root1.right, root2.right)
            if ans1 and ans2:
                return True
            else:
                return False

        stack = [root]
        while stack:
            pt = stack.pop()
            res = isequal(pt, subRoot)
            if res:
                return True
            if pt.left:
                stack.append(pt.left)
            if pt.right:
                stack.append(pt.right)

        return False

