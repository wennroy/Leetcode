# Definition for singly-linked list.
# class ListNode:
#     def __init__(self, x):
#         self.val = x
#         self.next = None

class Solution:
    def reverseList(self, head: ListNode) -> ListNode:
        pt = head
        stack = []
        while pt:
            stack.append(pt.val)
            pt = pt.next
        pt = head
        while pt:
            pt.val = stack.pop()
            pt = pt.next
        return head


